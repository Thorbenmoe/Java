public class UebungAufgabe2 
{
	public static void main(String[] args)
	{
		int i =1;
		for( i=1; i <=10; i++)
		{
			int j=1;
			for( j=1; j<=10; j++)
			{
				System.out.println("\t"+(i*j));
				
			}
			
			System.out.println();
		}
		
	}
	
}
